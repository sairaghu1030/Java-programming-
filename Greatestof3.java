public class Greatestof3 {
    public static void main(String[] args)
    {
        int a=7,b=45,c=18;
        if(a>b && a>c)
            System.out.println(a+" is greater ");
        if(b>c && b>a)
            System.out.println(b+" is greater ");
        if(c>c && c>a)
            System.out.println(c+" is greater ");
    }
}
